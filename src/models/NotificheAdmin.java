package models;

public class NotificheAdmin {
	
	int utente_id;
	String data;
	boolean mostra;
	
	public int getUtente_id() {
		return utente_id;
	}
	public void setUtente_id(int utente_id) {
		this.utente_id = utente_id;
	}
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
	public boolean getMostra() {
		return mostra;
	}
	public void setMostra(boolean mostra) {
		this.mostra = mostra;
	}
		
}
